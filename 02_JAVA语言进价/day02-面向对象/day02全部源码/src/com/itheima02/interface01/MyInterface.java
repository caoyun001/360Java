package com.itheima.interface01;

/**
 * 接口: 在Java中是一种标准规则
 * 在成员中全部定义抽象方法
 * 当一个类中,所有的方法全是抽象方法的时候,将这个类定义为一个更加
 * 贴切的定义,称为他接口. 理解为: 全部是抽象方法的类 (特殊的抽象类)
 *
 * 接口: 定义的关键字 interface
 *
 * 定义类class,编译完成是.class文件
 * 定义接口interface,编译完成后是.class文件
 */
public  interface MyInterface {
    public abstract void inter();
}
